package bean;

public class Shabi {
	private int id;
	private String name;
	private String ceshi;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCeshi() {
		return ceshi;
	}

	public void setCeshi(String ceshi) {
		this.ceshi = ceshi;
	}
}
